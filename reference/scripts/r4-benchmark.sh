#!/usr/bin/env bash
# ============================================================================
# R+4 proof-generation benchmark
# ----------------------------------------------------------------------------
# Runs the real R+4 proof pipeline and records timings + environment so the
# numbers are reproducible and honest. Run this on your build machine (Mac) —
# the circom toolchain cannot run in the DCS sandbox.
#
# Prereq: the toolchain is installed (circom 2.x, snarkjs, `npm install` done)
#         per COMPILE_RUNBOOK.md.
#
# Usage:
#   ./scripts/r4-benchmark.sh                # times prove + verify (artifacts must exist)
#   ./scripts/r4-benchmark.sh --with-build   # also compiles the circuit and times build.sh
#
# Output: writes R4_BENCHMARK_RESULT.md in the reference root — paste that back.
# ============================================================================
set -euo pipefail

cd "$(dirname "$0")/.."          # -> r4 reference root
ROOT="$(pwd)"
ART="$ROOT/artifacts"
OUT="$ROOT/R4_BENCHMARK_RESULT.md"
WITH_BUILD=0
[ "${1:-}" = "--with-build" ] && WITH_BUILD=1

hr(){ printf '%s\n' "------------------------------------------------------------"; }
now(){ date +%s; }

# ---- environment ----------------------------------------------------------
OS_NAME="$(uname -s)"
if [ "$OS_NAME" = "Darwin" ]; then
  CPU="$(sysctl -n machdep.cpu.brand_string 2>/dev/null || echo 'Apple Silicon')"
  CORES="$(sysctl -n hw.ncpu 2>/dev/null || echo '?')"
  MEM_GB="$(awk "BEGIN{printf \"%.0f\", $(sysctl -n hw.memsize 2>/dev/null || echo 0)/1073741824}")"
  OSV="$(sw_vers -productName 2>/dev/null || echo macOS) $(sw_vers -productVersion 2>/dev/null || echo '')"
else
  CPU="$(grep -m1 'model name' /proc/cpuinfo 2>/dev/null | cut -d: -f2 | xargs || echo unknown)"
  CORES="$(nproc 2>/dev/null || echo '?')"
  MEM_GB="$(awk '/MemTotal/{printf "%.0f", $2/1048576}' /proc/meminfo 2>/dev/null || echo '?')"
  OSV="$(uname -sr)"
fi
NODEV="$(node -v 2>/dev/null || echo 'not found')"
CIRCOMV="$(circom --version 2>/dev/null || echo 'not found')"
SNARKV="$(snarkjs --version 2>/dev/null | head -1 || echo 'not found')"

echo "R+4 proof-gen benchmark"
hr
echo "Host    : $CPU ($CORES cores, ${MEM_GB} GB)"
echo "OS      : $OSV"
echo "node    : $NODEV"
echo "circom  : $CIRCOMV"
echo "snarkjs : $SNARKV"
hr

# ---- optional: time the build -------------------------------------------
BUILD_FMT="(skipped — re-run with --with-build to time the compile)"
if [ "$WITH_BUILD" -eq 1 ]; then
  echo "[1/3] build.sh dev — circom compile + Groth16 setup ..."
  t0=$(now); ./build.sh dev; t1=$(now)
  BUILD_S=$((t1 - t0))
  BUILD_FMT="$((BUILD_S / 60))m $((BUILD_S % 60))s  (${BUILD_S}s)"
  echo "      build: $BUILD_FMT"
fi

if [ ! -f "$ART/threshold_count_final.zkey" ]; then
  echo "ERROR: artifacts/ not built — run ./build.sh dev first, or pass --with-build."
  exit 1
fi

# ---- constraint count ----------------------------------------------------
CONSTRAINTS="$(snarkjs r1cs info "$ART/threshold-count.r1cs" 2>/dev/null \
  | grep -i '# of Constraints' | sed 's/.*Constraints: *//' | xargs || echo '?')"

# ---- proof generation (timed) -------------------------------------------
echo "[2/3] proof generation — npm run prove ..."
t0=$(now)
npm run prove -- \
  --bundle example-bundle.json \
  --statement example-statement.json \
  --artifacts ./artifacts \
  --out ./proof.bench.json >/dev/null 2>&1
t1=$(now)
PROVE_S=$((t1 - t0))
PROVE_FMT="$((PROVE_S / 60))m $((PROVE_S % 60))s  (${PROVE_S}s)"
echo "      prove: $PROVE_FMT"

# ---- verification (capture the verifier's own timing line) --------------
echo "[3/3] proof verification — npm run verify ..."
VERIFY_OUT="$(npm run verify -- ./proof.bench.json 2>&1 || true)"
VERIFY_LINE="$(echo "$VERIFY_OUT" | grep -i 'groth16 verify' | head -1 | sed 's/^[[:space:]]*//' || echo 'see verify output above')"
echo "      verify: $VERIFY_LINE"

# ---- write the report ----------------------------------------------------
cat > "$OUT" <<EOF
# R+4 proof-gen benchmark — result

Generated by \`scripts/r4-benchmark.sh\` on $(date -u +"%Y-%m-%d %H:%M UTC").
Single run, dev keys, reference profile.

## Environment

| | |
|---|---|
| Host | $CPU |
| Cores / RAM | $CORES / ${MEM_GB} GB |
| OS | $OSV |
| node | $NODEV |
| circom | $CIRCOMV |
| snarkjs | $SNARKV |

## Circuit

| | |
|---|---|
| Circuit | threshold-count.circom |
| Constraints | $CONSTRAINTS |
| Profile | example-bundle.json — 5 signed receipts, depth-20 Poseidon-Merkle |

## Timings

| Step | Time |
|---|---|
| build.sh dev (compile + Groth16 setup) | $BUILD_FMT |
| Proof generation (groth16.fullProve) | $PROVE_FMT |
| Proof verification | $VERIFY_LINE |

> These are **dev-key** timings (single-contributor setup). They measure the
> proving/verifying cost, which the multi-party ceremony does not change.
> Report them as reference-hardware figures only — never as production trust.
EOF

hr
echo "Report written: $OUT"
echo "Paste R4_BENCHMARK_RESULT.md back to record the numbers."
